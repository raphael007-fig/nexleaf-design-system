// Bundle the shared design-system CSS into the Angular package's dist.
//
// The framework-agnostic `tokens.css` + `nx-*` component stylesheets are the
// single source of truth. React consumes them directly; this script
// concatenates them into `@nexleaf/angular/styles.css` so an Angular app gets
// the whole look with one import — instead of hand-importing each file. Run
// automatically after `ng-packagr` (see the build script).
//
// Order: tokens (custom properties) → global base → component CSS (alpha).
import { readFileSync, writeFileSync, copyFileSync, readdirSync, statSync, existsSync } from 'node:fs';
import { join, dirname, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));   // angular/scripts
const repoRoot = join(here, '..', '..');                // Design System/
const src = join(repoRoot, 'src');
const dist = join(here, '..', 'dist', 'nexleaf-angular');

if (!existsSync(dist)) {
  console.error('✗ dist not found — run ng-packagr before bundling styles.');
  process.exit(1);
}

function findCss(dir, acc = []) {
  for (const name of readdirSync(dir)) {
    const p = join(dir, name);
    if (statSync(p).isDirectory()) findCss(p, acc);
    else if (name.endsWith('.css')) acc.push(p);
  }
  return acc;
}

const tokens = join(src, 'tokens', 'tokens.css');
const globalCss = join(src, 'global.css');
const componentCss = findCss(join(src, 'components')).sort();
const ordered = [tokens, ...(existsSync(globalCss) ? [globalCss] : []), ...componentCss];

// CSS requires every `@import` to precede other rules. Since we concatenate
// many files, we hoist *remote* @imports (e.g. the Inter web font) to the top
// of the bundle and DROP *relative* @imports (e.g. `@import './tokens/...'`) —
// their targets are already inlined here, and a bundler can't resolve them.
const ANY_IMPORT = /^@import\b/i;
const REMOTE_IMPORT = /^@import\s+url\(\s*['"]?https?:/i;
const hoisted = new Set();

function stripImports(content) {
  return content
    .split('\n')
    .filter((line) => {
      const t = line.trim();
      if (!ANY_IMPORT.test(t)) return true;       // not an @import → keep
      if (REMOTE_IMPORT.test(t)) hoisted.add(t);  // remote (font) → hoist, keep
      return false;                               // relative @import → drop (inlined)
    })
    .join('\n');
}

const bodies = ordered.map((f) => {
  const css = stripImports(readFileSync(f, 'utf8')).trimEnd();
  return `/* ── ${relative(src, f)} ── */\n${css}`;
});

let out = '/* @nexleaf/angular — bundled design-system styles (tokens + nx-* component CSS).\n'
  + '   GENERATED by scripts/bundle-styles.mjs — do not edit by hand. */\n\n';
if (hoisted.size) out += [...hoisted].join('\n') + '\n\n';   // hoisted remote @imports first
out += bodies.join('\n\n') + '\n';
writeFileSync(join(dist, 'styles.css'), out);
copyFileSync(tokens, join(dist, 'tokens.css'));   // tokens-only, for apps that want just the vars

// Expose via package `exports` so apps can `@import '@nexleaf/angular/styles.css'`
// (or add it to angular.json "styles"). ng-packagr owns the JS exports; we add
// the CSS entries to the generated manifest.
const pkgPath = join(dist, 'package.json');
const pkg = JSON.parse(readFileSync(pkgPath, 'utf8'));
pkg.exports = pkg.exports || {};
pkg.exports['./styles.css'] = { style: './styles.css', default: './styles.css' };
pkg.exports['./tokens.css'] = { style: './tokens.css', default: './tokens.css' };
writeFileSync(pkgPath, JSON.stringify(pkg, null, 2) + '\n');

console.log(`✓ bundled ${ordered.length} stylesheets → styles.css (${(out.length / 1024).toFixed(1)} kB) + tokens.css; exports patched`);
